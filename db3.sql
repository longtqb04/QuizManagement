CREATE TABLE Student (
    STUDENTID     VARCHAR(7) PRIMARY KEY,
    FULL_NAME     VARCHAR(30) NOT NULL,
    BDATE         DATE,
    GENDER        CHAR(1),
    ADDRESS       VARCHAR(60) NOT NULL,
    PHONE         VARCHAR(10),
    EMAIL         VARCHAR(30) NOT NULL,
    FACULTY       VARCHAR(10)
);

CREATE TABLE Lecturer (
    L_CITIZENID     VARCHAR(12) PRIMARY KEY,
    L_FULL_NAME     VARCHAR(30) NOT NULL,
    L_BDATE         DATE,
    L_GENDER        CHAR(1),
    L_ADDRESS       VARCHAR(60) NOT NULL,
    L_PHONE         VARCHAR(10),
    L_EMAIL         VARCHAR(30) NOT NULL,
    L_FACULTY       VARCHAR(10)
);

CREATE TABLE Administrator (
    AD_ID       VARCHAR(10) PRIMARY KEY,
    AD_NAME     VARCHAR(30) NOT NULL,
    AD_EMAIL    VARCHAR(30) NOT NULL,
    AD_ROLE     VARCHAR(10)
);

CREATE TABLE Quizzes (
    QUIZ_ID       VARCHAR(6)  PRIMARY KEY,
    START_TIME    DATE,
    END_TIME      DATE,
    TIME_LIMIT    NUMBER(3),
    QUESTIONS     NUMBER(3)    
);

CREATE TABLE Questions (
    QUIZ_ID       VARCHAR(6),
    QUESTION_ID   VARCHAR(6) PRIMARY KEY,
    QUESTION      VARCHAR(1000),
    ANSWER_A      VARCHAR(1000),
    ANSWER_B      VARCHAR(1000),
    ANSWER_C      VARCHAR(1000),
    ANSWER_D      VARCHAR(1000),
    ANSWER_E      VARCHAR(1000),
    CORRECT       CHAR(1),

    CONSTRAINT fk_QuizID FOREIGN KEY (QUIZ_ID)
       REFERENCES Quizzes(QUIZ_ID)
       ON DELETE CASCADE
);

CREATE TABLE Scores (
    STUDENT_ID    VARCHAR(7),
    QUIZ_ID       VARCHAR(6),
    QUESTIONS     INT(3),
    CORRECT       INT(3),
    SCORE         FLOAT,

    CONSTRAINT fk_StuID_Scores FOREIGN KEY (STUDENT_ID)
       REFERENCES Student(STUDENTID)
       ON DELETE CASCADE,
    CONSTRAINT fk_QuizID_Scores FOREIGN KEY (QUIZ_ID)
       REFERENCES Quizzes(QUIZ_ID)
       ON DELETE CASCADE,
);

CREATE TABLE Accounts (
    USERNAME    VARCHAR(20) PRIMARY KEY,
    PASSWORD    VARCHAR(20)
);

CREATE TABLE ACC_Student (
    USERNAME    VARCHAR(20) PRIMARY KEY,
    STUDENTID   VARCHAR(7),

    CONSTRAINT fk_AccSTU FOREIGN KEY (USERNAME)
        REFERENCES Accounts(USERNAME)
        ON DELETE CASCADE 
);

CREATE TABLE ACC_Lecturer (
    USERNAME    VARCHAR(20) PRIMARY KEY,
    CITIZENID   VARCHAR(12),

    CONSTRAINT fk_AccLEC FOREIGN KEY (USERNAME)
        REFERENCES Accounts(USERNAME)
        ON DELETE CASCADE 
);

CREATE TABLE ACC_Student (
    USERNAME    VARCHAR(20) PRIMARY KEY,

    CONSTRAINT fk_AccADMIN FOREIGN KEY (USERNAME)
        REFERENCES Accounts(USERNAME)
        ON DELETE CASCADE 
);

